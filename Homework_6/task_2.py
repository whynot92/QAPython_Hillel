# Напишіть цикл, який буде вимагати від користувача ввести слово, в
# якому є літера "h" (враховуються як великі так і маленькі). Цикл не 
# повинен завершитися, якщо користувач ввів слово без букви "h".

while True:
    user_input = input("Введіть слово, що містить літеру 'h': ")
    
    if 'h' in user_input.lower():
        print("Ви ввели слово, що містить 'h'.")
        break
    else:
        print("Слово не містить літеру 'h'.")
